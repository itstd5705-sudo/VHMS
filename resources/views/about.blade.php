    document.addEventListener('DOMContentLoaded', function() {
    // 1. اختيار جميع البطاقات التي تحتاج إلى حركة باستخدام الكلاس الجديد
    const animatedCards = document.querySelectorAll('.massag-card.hidden');

    // 2. إعداد خيارات المراقب (Observer)
    const options = {
        root: null,
        threshold: 0.2,
        rootMargin: "0px"
    };// 3. إنشاء المراقب (Observer)
    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                // إذا أصبح العنصر مرئياً
                entry.target.classList.add('show');
                entry.target.classList.remove('hidden');
                // إيقاف مراقبة العنصر بعد ظهوره
                observer.unobserve(entry.target);
            }
        });
    }, options);

    // 4. تطبيق المراقب على كل بطاقة
    animatedCards.forEach(card => {
        observer.observe(card);
    });
});
