<script>document.addEventListener('DOMContentLoaded', () => {
    // لم نعد نحتاج إلى أزرار التحكم
    const carouselWrapper = document.querySelector('.carousel-wrapper');
    const slides = Array.from(document.querySelectorAll('.carousel-slide'));
    const navDotsContainer = document.querySelector('.carousel-nav-dots');

    if (!carouselWrapper || slides.length === 0) return;

    const slideCount = slides.length;
    let currentIndex = 0;
    let autoSlideInterval;
    const intervalTime = 4000; // الفترة الزمنية للتحريك التلقائي (4 ثواني)

    // 1. وظيفة تحديث الكلاسات (المسؤولة عن تأثير التمركز)
    const updateSlideClasses = () => {
        slides.forEach((slide, index) => {
            slide.classList.remove('active', 'prev', 'next');

            // تحديد حالة الشريحة
            if (index === currentIndex) {
                slide.classList.add('active');
            } else if (index === (currentIndex - 1 + slideCount) % slideCount) {
                // الشريحة السابقة
                slide.classList.add('prev');
            } else if (index === (currentIndex + 1) % slideCount) {
                // الشريحة التالية
                slide.classList.add('next');
            }
        });
        updateNavDots();
    };


    // 2. وظيفة إنشاء المؤشرات (النقاط)
    const createNavDots = () => {
        slides.forEach((slide, index) => {
            const dot = document.createElement('span');
            dot.classList.add('nav-dot');
            dot.setAttribute('data-index', index);
            dot.addEventListener('click', () => {
                moveToSlide(index);
                resetAutoSlide(); // عند النقر على نقطة، نعيد تشغيل المؤقت
            });
            navDotsContainer.appendChild(dot);
        });
    };

    // 3. وظيفة تحديث المؤشرات
    const updateNavDots = () => {
        const dots = Array.from(navDotsContainer.querySelectorAll('.nav-dot'));
        dots.forEach((dot, index) => {
            dot.classList.toggle('active', index === currentIndex);
        });
    };

    // 4. وظيفة التحريك الأساسية
    const moveToSlide = (targetIndex) => {
        currentIndex = targetIndex;
        updateSlideClasses();
    };

    // 5. وظيفة التحريك التلقائي إلى الشريحة التالية
    const moveToNextSlide = () => {
        const newIndex = (currentIndex + 1) % slideCount;
        moveToSlide(newIndex);
    };

    // 6. وظيفة بدء التحريك التلقائي
    const startAutoSlide = () => {
        // التحريك التلقائي إلى الشريحة التالية كل 4 ثواني
        autoSlideInterval = setInterval(moveToNextSlide, intervalTime);
    };

    // 7. وظيفة إعادة تعيين المؤقت بعد تفاعل المستخدم (عبر النقاط)
    const resetAutoSlide = () => {
        clearInterval(autoSlideInterval);
        startAutoSlide();
    };

    // تهيئة المعرض عند تحميل الصفحة
    createNavDots();
    updateSlideClasses();
    startAutoSlide();
});</script>
